package CodingDay;

public class Main{
    public static void main(String[]args){
        
        int w;
        
        w =10;//pengisian nilai
        
        w+=5;//penugasan penambahan
        System.out.println("penugasan += "+w);
        
        w =10;
        w-=5;//penugasan pengurangan
        System.out.println("penugasan -= "+w);
    }
    
}
